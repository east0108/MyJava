package tw.brad.myjava;

public class Brad02 {

	public static void main(String[] args) {
		// 整數 byte: 2^8, short: 2^16 , int: 2^32, long: 2^64
		//強型別
		byte var1 = -127; // 2^8 => 256 => -128 ~ 127
		byte Var1 = -123; // [a-zAZ$_][a-zA-Z0-9$_]* + no keyword
		byte $$$;
		byte $_$;
		$$$ = -1; $_$ = 27;
		System.out.println(var1);
		System.out.println(Var1);
		System.out.println($$$);
		System.out.println($_$);
	}

}
